<?php

namespace App\Entity;

/**
 * This class stores data about person responsible for the job listing
 * @package App\Entity
 */
class Personalist
{
    private int $id;
    private string $name;

    public function __construct(int $id, string $name)
    {
        $this->id = $id;
        $this->name = $name;
    }

    public function getId(): int
    {
        return $this->id;
    }

    public function getName(): string
    {
        return $this->name;
    }
}
